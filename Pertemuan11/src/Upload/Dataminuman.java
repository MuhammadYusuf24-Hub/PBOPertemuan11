    private void buploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buploadActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();

            // Ambil koneksi dari persistence.xml
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("Pertemuan11PU");
            EntityManager em = emf.createEntityManager();

            try (BufferedReader br = new BufferedReader(new FileReader(file))) {
                em.getTransaction().begin(); // mulai transaksi

                String line;
                int count = 0;

                while ((line = br.readLine()) != null) {
                    if (line.trim().isEmpty()) {
                        continue; // lewati baris kosong
                    }
                    String[] data = line.split(";");
                    if (data.length == 5) {
                        try {
                            Dataminuman_1 minuman = new Dataminuman_1();
                            minuman.setIdMinuman(Integer.valueOf(data[0].trim()));
                            minuman.setNama(data[1].trim());
                            minuman.setKategori(data[2].trim());
                            minuman.setHarga(Integer.valueOf(data[3].trim()));
                            minuman.setUkuran(data[4].trim());

                            em.persist(minuman); // simpan ke database
                            count++;

                        } catch (NumberFormatException e) {
                            System.out.println("Baris dilewati (angka salah): " + line);
                        }
                    } else {
                        System.out.println("Format salah: " + line);
                    }
                }

                em.getTransaction().commit(); // simpan semua data ke DB

                JOptionPane.showMessageDialog(this,
                        "Upload berhasil! " + count + " data dimasukkan.",
                        "Sukses", JOptionPane.INFORMATION_MESSAGE);

                tampilData(); // refresh tabel GUI

            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Kesalahan saat membaca file: " + e.getMessage());
            } catch (Exception e) {
                if (em.getTransaction().isActive()) {
                    em.getTransaction().rollback();
                }
                JOptionPane.showMessageDialog(this, "Kesalahan upload: " + e.getMessage());
            } finally {
                em.close();
                emf.close();
            }

        } else {
            JOptionPane.showMessageDialog(this, "Upload dibatalkan.", "Informasi", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_buploadActionPerformed

