    private void tampilData() {
        // Membuat koneksi EntityManager dari persistence unit
        EntityManager em = Persistence.createEntityManagerFactory("Pertemuan11PU").createEntityManager();

        try {
            // Jalankan NamedQuery dari Entity Dataminuman
            Query q = em.createNamedQuery("Dataminuman_1.findAll");
            List<Dataminuman_1> hasil = q.getResultList();

            // Membuat model tabel baru dengan kolom yang tidak bisa diedit
            DefaultTableModel model = new DefaultTableModel(new String[]{"ID", "Nama", "Kategori", "Harga", "Ukuran"}, 0) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false; // semua kolom tidak bisa diedit langsung
                }
            };

            // Isi tabel dengan data hasil query
            for (Dataminuman_1 data : hasil) {
                Object[] baris = new Object[5];
                baris[0] = data.getIdMinuman();
                baris[1] = data.getNama();
                baris[2] = data.getKategori();
                baris[3] = data.getHarga();
                baris[4] = data.getUkuran();
                model.addRow(baris);
            }

            // Set model ke JTable kamu
            jtab.setModel(model);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menampilkan data: " + e.getMessage());
        } finally {
            em.close(); // Tutup koneksi EntityManager
        }
    }

